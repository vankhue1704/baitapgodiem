const express = require('express');
const auth = require('../../middlewares/auth');
const validate = require('../../middlewares/validate');
const DonhangValidation = require('../../validations/donhang.validation');
const DonhangController = require('../../controllers/donhang.controller');

const router = express.Router();

router
  .route('/')
  .post(validate(DonhangValidation.createDonhang), DonhangController.createDonhang)
  .get(validate(DonhangValidation.getDonhangs), DonhangController.getDonhangs);

router
  .route('/:id')
  .get(auth('getDonhangs'), validate(DonhangValidation.getDonhang), DonhangController.getDonhang)
  .patch(validate(DonhangValidation.updateDonhang), DonhangController.updateDonhang)
  .delete(validate(DonhangValidation.deleteDonhang), DonhangController.deleteDonhang);

module.exports = router;

